package utils;

/**
 * The {@code Color} enum represents the possible colors of cards in a game of Uno.
 * Each color has a distinct representation and is used to categorize and identify cards in the game.
 * <p>
 * The enum includes the following colors:
 * <ul>
 *     <li>{@code RED}: Represents the color red.</li>
 *     <li>{@code GREEN}: Represents the color green.</li>
 *     <li>{@code BLUE}: Represents the color blue.</li>
 *     <li>{@code YELLOW}: Represents the color yellow.</li>
 * </ul>
 * </p>
 * <p>
 * Each color provides a visual and thematic element to the cards, allowing players to distinguish
 * between different types of cards during the game.
 * </p>
 *
 * @author Riley Woolf
 * @version 1.0
 */
public enum Color {
    /**
     * Represents the color red.
     */
    RED,

    /**
     * Represents the color green.
     */
    GREEN,

    /**
     * Represents the color blue.
     */
    BLUE,

    /**
     * Represents the color yellow.
     */
    YELLOW
}
